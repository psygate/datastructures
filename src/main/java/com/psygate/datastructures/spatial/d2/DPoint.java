package com.psygate.datastructures.spatial.d2;

import com.psygate.datastructures.spatial.d2.IDPoint;

/**
 *
 * @author psygate (https://github.com/psygate)
 */
public final class DPoint implements IDPoint {

    private final double x, y;

    public DPoint(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public DPoint(final IDPoint point) {
        this.x = point.getX();
        this.y = point.getY();
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 59 * hash + (int) (Double.doubleToLongBits(this.x) ^ (Double.doubleToLongBits(this.x) >>> 32));
        hash = 59 * hash + (int) (Double.doubleToLongBits(this.y) ^ (Double.doubleToLongBits(this.y) >>> 32));
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final DPoint other = (DPoint) obj;
        if (Double.doubleToLongBits(this.x) != Double.doubleToLongBits(other.x)) {
            return false;
        }
        if (Double.doubleToLongBits(this.y) != Double.doubleToLongBits(other.y)) {
            return false;
        }
        return true;
    }

    @Override
    public double getX() {
        return x;
    }

    @Override
    public double getY() {
        return y;
    }

    @Override
    public String toString() {
        return "DPoint{" + "x=" + x + ", y=" + y + '}';
    }

}
